            <p style="font-size:14px; color:#000;"> SDMCET is around three kilometers from main/city bus-stand and railway station.Auto rickshaws charge roughly around Rs 30 to 50(depending upon the time of the day) to reach SDM college of Engineering and Technology,Dharwad from these destinations. </p>
            <p style="font-size:16px; color:#F00;"> What after reaching SDMCET? </p>
            <p style="font-size:14px; color:#000;"> If you are registered on the hospitality portal of INSIGNIA'12, then you need to come to the Registration Desk for registration, which will be set up at the entrance of the main building of the campus.You will be provided the accommodation only if you have registered for the accommodation. </p>
            <p style="font-size:16px; color:#F00;"> How do I get accommodation? </p>
            <ul>
              <li>While registering for the events, participants (who wants accommodation during the event) has to opt   for accommodation in the registration page.</li>
              <li>Accommodation shall be provided at a cost of Rs 50/- per participant per day.</li>
              <li>Accommodation will  be provided separately for boys and girls at different partner institutions. </li>
              <li>Accommodation  will be provided from the day of start of fest till it gets over.</li>
              <li>Participants should note that they will be required to share accommodation. Requests for individual rooms or any other complaints in this regard will not be entertained. Also the accommodation will be restricted only to the students.</li>
            </ul>
All participants are required to carry their college identity cards. The participants are also expected to follow certain Rules and Regulations to help us maintain the right kind of festive atmosphere in the institute.<br />
<br />
All essential facilities are present inside the campus.
</p>
<p style="font-size:16px; color:#F00;"> What should I bring with me? </p>
<ul>
  <li>It is mandatory for all participants to carry college IDs.</li>
  <li>Mattresses, pillows, bed spreads and buckets are the only things that shall be provided to you for accommodation. All the other things which participants require for their stay here should be arranged by themselves.</li>
</ul>
<p style="font-size:16px; color:#F00;"> Where do I eat? </p>
<p style="font-size:14px; color:#000;"> The festive area will have INSIGNIA'12 Food court to take care of your appetite (food coupons will be provided at the time of registering for the events at Registration Desk).<br />
  <br />
  There are lots of eating joints in the campus -Santrupti(Central Dining For Students) SDMCET Food Court, SDMCET  Bakery, Suruchi-College Canteen and F&amp;H Coffee Shop. </p>
<p style="font-size:16px; color:#F00;"> What about the medical assistance? </p>
<p style="font-size:14px; color:#000;"> In case you fall ill, you are advised to report to the volunteers of INSIGNIA'12. We shall make appropriate arrangements for your treatments. </p>
<p style="font-size:16px; color:#F00;"> What about the security facilities? </p>
<p style="font-size:14px; color:#000;"> SDMCET campus has a vigilant and round-the-clock security service. You have to take care of your belongings and we are not responsible for the loss. </p>
<p style="font-size:16px; color:#F00;"> What all things are prohibited in the campus? </p>
<p style="font-size:14px; color:#000;"> Consumption of alcohol, tobacco, non-vegetarian food or any intoxicants is strictly prohibited within campus premises. Any flammable liquids, knives and explosives of any kind are strictly prohibited inside the campus. Any other item if deemed unsafe will be prohibited. </p>
