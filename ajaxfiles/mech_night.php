Rules and regulations:
</p>
 <p>&nbsp;</p>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li>Participants should carry their college IDs, INSIGNIA IDs and event coupons.</li>
<li>The event will be conducted on an individual level.</li>
<li>The objective will be to create solid and assembly modeling.</li>
<li>Allotted time for modeling starts from 10pm and designs should be submitted by 6 am.</li>
<li>Final parts and assembly designs should be saved on the respective systems provided to the teams.</li>
<li>Decision of judges would be considered final.</li>
<li>Teams are allowed to work on Solid Edge, CATIA, ProE, AutoCAD and UniGraphics.</li>
</ul>
<p style="font-size:14px; color:#FFF;">
Rules may be subjected to change if necessary and will be specified from time to time by the event staff coordinator.
</p>
<p style="font-size:14px; color:#FFF;"> Refreshments will be provided to the participants during the time of the event. </p>
<p style="font-size:18px; color:#F00;">
Procedure:
</p>
 <p>&nbsp;</p>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li>All the registered participants will be provided with individual workstations consisting of all work tools as mentioned above.</li>
<li>Participants should complete the modeling within the specified time limit. There will no extension of time limit.</li>
<li>Print outs of the final models are to be taken and submitted to the coordinators and judges.</li>
</ul>
