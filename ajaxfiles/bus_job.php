Every great business is sustained by great leaders. Great men, who never shy away from responsibility and take tough, heart-breaking decisions, not everyone has the grit to run a business. JOBS’ Analysts gives you an opportunity to see if you have what it takes to be the best. The event is not for the common technical brain but for people with the bent of mind to look at the world from a viewpoint which is beyond the presumed physical laws, it is about logic and faith in your abilities. <p>&nbsp;</p>
The event consists of three rounds and each team consists of 2 members. The teams need to show their marketing knowledge and managerial skill. 
</p>



<p style="font-size:18px; color:#F00;">
Round 1 :
</p>
 <p>&nbsp;</p>
<p style="font-size:14px; color:#FFF;  margin-left:20px;">
From Drucker to Chanakya from the 80-20 Rule to the Network Effect this quiz promises to be an extravaganza of business and management as the brightest minds from India’s premier institutes fight it out for the top honours. Prove that what matters to you the most is the know-how of the corporate sector in the form of a written quiz. The duration of quiz will be <b>45 minutes</b>.
</p>
 <p>&nbsp;</p>

<p style="font-size:18px; color:#F00;">
Round 2 :
</p>
<p style="font-size:14px; color:#FFF;  margin-left:20px;">
