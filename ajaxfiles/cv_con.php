Rules and regulations:
</p>
 <p>&nbsp;</p>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li>Two participants per team is compulsory.</li>
<li>An Elimination round will be held depending upon no. of participating teams.</li>
<li>Judging Criteria</li> 
</ul>
<ul style="font-size:14px; color:#FFF; list-style:none; margin-left:80px;">

<li> 1. Accuracy </li>
 <li>2. Quickness</li>
<li>  3. Percentage completion of work</li>
 <li> 4. Practical Knowledge</li>
 </ul>
 <ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li>Judges decision is final and binding.</li>
</ul>
<p>&nbsp;</p>
<p style="font-size:18px; color:#F00;">
Procedure:
</p>
 <p>&nbsp;</p>

<p style="font-size:16px; color:#F00;">
Stage 1- Survey
</p>
<p style="font-size:16px; color:#F00;">&nbsp;</p>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li>The teams shall be given a problem statement and they shall be provided with equipments necessary for carrying out the survey.  </li>
<li>Time limit – 20 mins </li>
</ul>
<p style="font-size:16px; color:#F00;">
Stage 2- Estimate
</p>
<p style="font-size:16px; color:#F00;">&nbsp;</p>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li> The teams shall be provided with a detailed plan of a building component and they are required to prepare an estimate involving quantity of materials and the total rate of work.</li>
<li> The teams are required to have knowledge of current schedule of rates.</li>
<li> Time limit – 20 mins.</li>
</ul>
<p style="font-size:16px; color:#F00;">
Stage 3- Laboratory
</p>
<p style="font-size:16px; color:#F00;">&nbsp;</p>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li> The teams shall choose a specialization of their interest ( Environmental engg, Geotechnical Engg, Hydraulics, Concrete Tech..etc).</li>
<li> A topic of that specialization shall be provided to the teams along with necessary laboratory equipments and they are required to perform the experiments in specified time.  </li>
<li> Time limit – 20 mins</li>
</ul>
<p style="font-size:16px; color:#F00;">
Stage 4- Concrete Design
</p>
<p style="font-size:16px; color:#F00;">&nbsp;</p>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li> The teams shall be provided with a problem statement and they are required to design the Concrete Member within specified time.</li>
<li> Time limit – 20 mins.</li>
</ul>
