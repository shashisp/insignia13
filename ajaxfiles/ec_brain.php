           <p style="font-size:18px; color:#F00;">Problem definition:</p>
<p style="font-size:14px; color:#FFF;">
Development of Home Automation System using Digital Clock Manager</p>
 <p>&nbsp;</p>
<p style="font-size:18px; color:#F00;">
Prime objectives:
</p>
<p>&nbsp;</p>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li>Development of Digital Clock using either 8085/8086/8051 or any other processor/controller.</li>
<li>The minimum facilities/features of Digital Clock are displaying of Time with Hour, Minute, Seconds, 
    AM and PM, Alarm indication
</li>
<li>The Digital Clock must be integrated also with Temperature Sensor, Humidity Sensor, Light Intensity 
    Sensor
</li>
<li>The Digital Clock should display the present room temperature using temperature sensor and also 
    display the humidity levels appropriately using humidity sensors
</li>
<li>Using temperature sensor, the system should sense the temperature and control the fan present in the 
    room, based on the requirement
</li>
<li>The light intensity sensor has to detect the natural and artificial light present in the room and the 
     system must control the lighting of the room appropriately.</li>
</ul>
<p>&nbsp;</p>
 <p style="font-size:18px; color:#F00;">
Minimum Objectives
</p>
 <p>&nbsp;</p>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li>The design of Digital Clock must be done by considering Prime Objective 1 and Prime Objective 2</li>
<li>The working hardware model of the system is preferred for the evaluation.</li>
<li>Any innovation and development in-line with prime objectives will be considered for the evaluation.</li>
</ul>
<p>&nbsp;</p>
<p style="font-size:18px; color:#F00;">
Rules and Regulations:
</p>
<p>&nbsp;</p>

<ul style="font-size:14px; color:#FFF;  margin-left:20px;">

<li>The team must contain minimum of <b>two (02)</b> and <b>maximum of three (03)</b> participants for the participation in the event.</li>
<li>If the <b>prime objectives</b> are incompletely executed and <b>minimum objectives</b> are carried out, in such circumstances, <b>presentation of innovation / idea</b> is entertained for the expression of views.</li>
<li>Judges decision is final for the award of prizes.</li>
</ul>
<p>&nbsp;</p>


<p style="font-size:18px; color:#F00;">
Note:
</p>
<p>&nbsp;</p>

<ul style="font-size:14px; color:#FFF;  margin-left:20px;">

<li>No microcontroller programmer / burner will be provided for programming.</li>
<li>Any available language (assembly / c-program) can be utilized for coding the controller.</li>
<li>Assume necessary details (if any).</li>
<li>For more details, feel free to contact the student and faculty co-ordinators.</li>

</ul>
