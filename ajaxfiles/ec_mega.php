           <p style="font-size:18px; color:#F00;">PROBLEM STATEMENT: </p>
          <p style="font-size:14px; color:#FFF;">
          A fully autonomous simple line tracking/following robot has to be made/Assembled which can be programmed to travel a path that runs through junctions, unusual curves and pointed angles. The autonomous line following machine should follow the line without deviating from it and should maintain the flow of direction which is indicated on the arena.
        </p>
          
  <p style="font-size:18px; color:#F00;">&nbsp;
       </p>
  <p style="font-size:18px; color:#F00;">ARENA DESCRIPTION </p>
  
 <ul style="font-size:14px; color:#FFF;">
            
        <li>   The arena is divided in two regions a) BLACK region and b) WHITE region.</li>
<li>The one half of base of full arena is made up of black colored Background (BLACK region)  of  Vinyl sheet on which white colored tracks are pasted and the another half part of arena is made up with White colored background (WHITE region) of  Vinyl sheet on which Black colored tracks are pasted.</li>
<li>The thickness of the Track is fixed to 30mm approximately (the thickness may sometimes slightly vary from 29mm to 31mm).</li>
<li>The tracks are made up of Vinyl sheet of color mentioned in above statement.</li>
<li>The track line may have angles not less than 30 degrees, unusual curves, loops and continuous tracks from white region to black region and vice versa.  </li>
<li>The arena consists of START & STOP points and has a dimension of Breadth -30cm & Length-30cm.</li>
<li>Arena for both the qualifying & final round will be displayed on the event day only.</li>

 </ul>
 
 <p style="font-size:18px; color:#F00;">&nbsp;
      </p>
 <p style="font-size:18px; color:#F00;">ROBOT SPECIFICATIONS </p>
 <p style="font-size:14px; color:#FFF;">Dimensional specifications:</p>
 <ul style="font-size:14px; color:#FFF; margin-left:60px;">
  <li> The robot should fit in a cube of 20 cm and weight is at choice. </li>              
  </ul>
  <p style="font-size:14px; color:#FFF;">Control specifications:</p>
 <ul style="font-size:14px; color:#FFF; margin-left:60px;">
  <li> The robot should be fully autonomous (fully automatic).</li>              
  </ul>
  <p style="font-size:14px; color:#FFF;">Power specifications:</p>
 <ul style="font-size:14px; color:#FFF; margin-left:60px;">
  <li> The robot should have on-board power supply and voltage between any two points on the robot should not exceed 12V.</li>              
  </ul>            
 
          <p style="font-size:14px; color:#FFF;">
     <b> <u>Note:</u> A tolerance of 5% is allowed in the above mentioned dimensional and power specifications, taking into consideration the error of the measuring instruments that we use here and those used by the participants in making of the robot.</b>
</p>
          

<p style="font-size:18px; color:#F00;">&nbsp;
       </p>
<p style="font-size:18px; color:#F00;">THE EVENT: </p>
          <p style="font-size:14px; color:#FFF;">
          The event will be conducted in two rounds each of increasing complexity, among which the first will be the QUALIFYING ROUND & second will be the FINAL ROUND.</p>
            <p style="font-size:18px; color:#F00;">&nbsp;        </p>
            <p style="font-size:18px; color:#F00;">QUALIFYING ROUND:</p>
        <ul style="font-size:14px; color:#FFF;">
      <li>  Each participant will be given a TWO chance to run his / her robot on any one half part of the arena for QUALIFYING ROUND (refer to figure -1).</li>
<li>The best of one run will be considered and the robot which is not capable to begin from the START/STOP point the Robot will gain penalty.</li>
<li>Participants can choose the START/STOP points for the Qualifying round.(i.e. Start with Black Region and end  up at the Start of  White Region and vice versa )</li> 
<li>To qualify in this round, Robot has to travel at least one half of the arena(i.e. begin from A and end at B.. ref to figure 1) </li> 
<li>This round consists of simple continuous tracks, curves, angles not less than 90 degrees.</li>
</ul>
<img src="images/ec_arena.jpg" alt="" width="350" height="230">
<p style="font-size:14px; color:#FFF;">Figure 1: Sample for QUALYFYING round</p>

        <p style="font-size:18px; color:#F00;">&nbsp;       </p>
        <p style="font-size:18px; color:#F00;">FINAL ROUND:</p>
        <ul style="font-size:14px; color:#FFF;">
   <li>   Final round is a KNOCKOUT round.
<li>Only one chance will be given to each team to run their robot in the final round.
<li>Participants need to pick a chit to choose START point for the final round.(i.e. begin with START point on  Black Region and end  with STOP point in White Region and vice versa ). </li>
<li>Robot has to travel from start point to the stop point without deviating from the track.</li>
<li>This round may have angles not less than 30 degrees, curves, loops and continuous tracks from black to white region and vice versa.</li>
<li>The robot need to begin from the center of  START point & end at the center of STOP point, if it fails to do so then penalty will be added. </li>
</ul>
<img src="images/ec_arena11.jpg" alt="" width="350" height="250">

<p style="font-size:14px; color:#FFF;">Figure 2: Sample arena for FINAL round.</p>

    <p style="font-size:14px; color:#FFF;">  <u>  NOTE:</u>  Information regarding the points will given on the event day itself. This criterion holds only for both qualifying & final round.</p>
    
     <p style="font-size:18px; color:#F00;">&nbsp;      </p>
     <p style="font-size:18px; color:#F00;">RULES AND REGULATIONS:</p>
 <ul style="font-size:14px; color:#FFF;">
  <li>   A maximum of 4 participants and minimum of 2 participants are allowed in a team.</li>
<li>The Robot should be completely autonomous and it should not receive any sort of external assistance.</li> 
<li>All kinds of programmable kits are allowed in this event. </li>
<li>At event rounds only two participants with robot are allowed in the arena.</li> 
<li>The Robot must deal with lighting conditions as they appear. No changes will be made individually to any participant. IR focusing cameras and flash photography will be allowed unless judges deem such activities. </li>
<li>Judges and coordinators reserve the right to ask for the explanation of the robot. Judges also reserves a right to stop a run and disqualify the robot. </li>
<li>Do not make any assumptions about the amount or type of light present at the site. However a chance for calibration of the sensors shall be provided.</li>
<li>All the robots will be racing on the same track for a given round. </li>
<li>The number of robots qualified for the next round will be revealed prior to the next contest.</li>
<li>Participation certificates will be given for those who clear the qualifying round.  </li>
<li>A maximum of 4 participants are allowed per team. Participants may be from different colleges as well.</li>
<li>No team is allowed to check their robot trials on the main arenas. We will provide a small test arena similar to the main arena to test their robot.</li>
<li>Dimensions of the arena are strictly obeyed & if any minor changes in case, then it shall be informed.</li>
<li>Do-not assume the context of arena, better contact the coordinators for more information & to clarify your problems.</li>
</ul>
 <p style="font-size:18px; color:#F00;">&nbsp;     </p>
 <p style="font-size:18px; color:#F00;">JUDGING CRITERIA:</p>
      
       <ul style="font-size:14px; color:#FFF;">
       
     <li>  The basic criterion is maximum distance covered in least possible time. The robot that reaches STOP point or covers maximum distance in minimum time summing with penalty will be declared as winner.</li>
<li>If the robot deviates from the line, the participant will get only one chance to place his robot on the track specified by the coordinators on the given check point.</li>
<li>Restarting the robot in check point at the time of diversion from arena will lead to a penalty.</li>
<li>If the robot is not able to make a perfect start & perfect stop then a penalty will be added.</li>
<li>The details regarding the penalties will be declared on the day of event.</li>
<li>Game Points will be given to each team which is purely based on maximum distance travelled in least possible time.</li>

       </ul>
