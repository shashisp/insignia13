Rules and regulations:
</p>
<p>&nbsp;</p>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li>A team consists of 2 members.</li>
<li>The First Round will consist of Round Table Debate.</li>
<li>Each speaker will be given 3 minutes to put forth his argument.</li>
<li>Selected teams will qualify for the next round, based on their scores that will be normalised with respect to the other debates.</li>
<li>the selected teams that qualify for the 2nd round will debate an issue/crisis, with either member of each team being compulsorily for or against the motion.</li>
<li>In the 2nd round, each participant will make 3 – 5 minute speeches followed by rebuttal of 3 minutes.</li>
<li>The decision of the judges will be final and binding.</li>
<li>The points shall be based on clarity of thought, speech and confidence.</li> 
