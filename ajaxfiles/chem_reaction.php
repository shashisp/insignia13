<p style="font-size:18px; color:#F00;">
The event consists of two rounds :
</p>
 <p>&nbsp;</p>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li>Round 1 : Participant should explain the working and design of the boat to the Judge and give a trial.</li>
<li>Round 2 : Two runs will be given and the best one will be considered. </li>
</ul>

 <p>&nbsp;</p>



<p style="font-size:18px; color:#F00;">
Rules and regulations:
</p>
 <p>&nbsp;</p>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li>Team should consist of 4 members.</li>
<li>The contestants are required to send a soft copy of their Abstract to the insignia email id provided: chem.ccr.insignia13@gmail.com (not more than 100 words).</li>
<li>The boat should be self-propelled using only chemical reactions or processes (Not      Batteries). No other power is allowed for propulsion of the boat.</li>
<li>The Boat should not touch the tank boundary</li>
<li>Control system can be designed to ensure that the boat does not touch the boundary.</li>
<li>Actuators or motors can be used to control the path and direction of the boat.</li>
<li>Participants should get  all the necessary things required for running of their boat.(Boat equipment, Chemicals, controllers, etc) </li>
<li>Judges should be convinced that the control system is used only for the steering Purpose and not for  propelling the boat in the tank.</li>
<li>Time for crossing the length (2.5 meters) is noted down to decide the final result.</li>

</ul>
