<p style="font-size:14px; color:#FFF;">
<i><b>Entrepreneurship is often the most difficult but also the most courageous and risk taking decision in an individual’s life. An efficient and practical Business plan leads an Entrepreneur to the path of success. We at SDMCET have organized this event INCORPORATED (under INSIGNIA-13) in the hope of encouraging the mindset of entrepreneurship and celebrating the cultural unity of calculated risk takers.</b></i>
</p>
 <p>&nbsp;</p>


<p style="font-size:18px; color:#F00;">
</p>
 <p>&nbsp;</p>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li>THE INCORPORATED encourages the mindset of entrepreneurship by providing an opportunity to prune and polish the ideas of youngsters as entrepreneurs.</li>
<li>THE INCORPORATED provides them a platform to prove themselves how well versed they are in the present Indian market as well as face the upcoming challenges which the market has to offer them.</li>
<li>Perfection in every aspect of planning is one of the challenges that INCORPORATED throws on the intellect of the participants.</li>
<li>Hence participate with full spirit and enthusiasm to enhance your vision and broaden your outlook in entrepreneurship. </li>

</ul>
 <p>&nbsp;</p>


<p style="font-size:16px; color:#FFF;  margin-left:20px;">
<b>The event would be divided into following phases:</b>
</p><p>&nbsp;</p>



<p style="font-size:18px; color:#F00;">
Phase 1 (Submission of Executive Summary):
</p>
 <p>&nbsp;</p>
<p style="font-size:14px; color:#FFF;  margin-left:20px;">
The Executive Summary is known to be the most important aspect of any business plan. It summarizes the key aspects of the business plan viz. business proposition, key financial projections and also the business idea and the strategy to implement it.</p>
 <p>&nbsp;</p>



<p style="font-size:18px; color:#F00;">
Phase 2 (Final Presentation):
</p>
 <p>&nbsp;</p>
<p style="font-size:14px; color:#FFF;  margin-left:20px;">
Teams would be presenting their complete business plan in front of a panel of judges on the final day.</p>
 <p>&nbsp;</p>





<p style="font-size:18px; color:#F00;">
Rules :
</p>
 <p>&nbsp;</p>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li>Participants need to Register at www.insignia13.com</li>
<li>Each Team must specify a Team Name.</li>
<li>The contest is open for all the under-graduates and post-graduates across India.</li>
<li>A team will consists on 3-5 members.</li>
<li>A team can consists of members from different colleges.</li>
<li>One person can be a part of only 1 team.</li>
<li>A team can submit multiples plans but they need to register separately for each plan.</li>
<li>Teams are allowed to submit their B-Plans in other competitions also.</li>
<li>Any kind of plagiarism would result in immediate disqualification of the team.</li>
<li>Every participant must abide by the rules and regulations of the event.</li>
<li>In case of any dispute, the decision of the Coordinating Committee would be final.</li>
<li>All conflict subject to Jurisdiction of Hubli Court only.</li>

</ul>
 <p>&nbsp;</p>




<p style="font-size:18px; color:#F00;">
Rules for Submitting Executive Summary: 
</p>
 <p>&nbsp;</p>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li>The Executive summaries should be mailed at incorporated.insignia13@gmail.com</li>
<li>All the summaries to be submitted before 17 March 2013.</li>
<li>The summary should include the following :</li>
<li>The Idea – its innovativeness, uniqueness and feasibility</li>
<li>Investment Strategy – Required capital and ways to procure it</li>
<li>Examining the Market Scenario – Market, Potential Competitors and ways to tackle them</li>
<li>Potential market Size and Growth – predetermining the growth potential of your plan</li>
<li>Uniqueness of Product/Service</li>
<li>Revenue Mechanism and Profit Expectations</li>
<li>Marketing and Sales Strategy</li>
<li>Any research involved like public surveys.</li>
<li>Exit Strategy (in case of failure)</li>
<li>On the whole highly persuasive in all aspects. A business concept with great potential, clear prospects for success.</li>
<li>The summary should be of max. 6 pages including the front page.</li>
<li>The font size should be 16 with font style as Calibri. </li>
</ul>
