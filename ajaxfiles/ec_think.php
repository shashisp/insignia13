         <b><i> No question is silly……!!!</i></b>
           </p>
          <p style="font-size:18px; color:#F00;">
Rules and regulations:
</p>
 <p>&nbsp;</p>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li>Team size: Two members per team</li>
<li>Classical dance can also be performed this category.</li>
<li>Maximum time duration is 10 minutes.</li>
<li>Audio CD's or PEN DRIVES to be provided by the participants to the event coordinator in ready to play condition, at least 30 minutes before event begins.</li>
<li>There will be 2 rounds namely prelims and finals.</li>
</ul>
 <p>&nbsp;</p>

<p>&nbsp;</p>
<p style="font-size:18px; color:#F00;">
1.ELIGIBILITY TEST
</p>
<p style="font-size:14px; color:#FFF;">
This consists of 30 technicalquestions (electronics based) which have to be solved within 45 minutes time span. The questions will be in the form of multiple choices; fill in the blanks; give reasons and puzzles.
<p style="font-size:14px; color:#FFF;">Screening will be done on the basis of the performance of the participant teams in the eligibility test. The top five teams will make it to the on stage QUIZ contest.
<p style="font-size:14px; color:#FFF;"> <b> Note: In case of tie between 2 or more teams, the teams who have solved ‘Puzzles’ and ‘Give Reasons’ will be given preference.</b>
  
  
  
  
<p style="font-size:18px; color:#F00;">
2.QUIZ CONTEST
</p>
<p style="font-size:16px; color:#F00;">
<b>Pre-Quiz Formalities</b>
</p>
 <ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li><b>Quiz is completely based on electronics and technology trends.</b></li>
<li><b>Rules and Regulations of the quiz will be briefed by the quiz master.</b></li>
<li><b>Each and every team will be given 2 Red Cards and a Yellow card.</b></li>
</ul>
 <p>&nbsp;</p>
<p style="font-size:18px; color:#F00;">
<b>Concept of using Red and yellow Cards:</b>
</p>
<p style="font-size:14px; color:#FFF;">
During the course of Quiz, teams are free to avail these facilities.
</p>
<p>&nbsp;</p>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li>The Red cards can be used to challenge any other team with the same question.</li>
<li>This facility can be used only in round 4 with each card used only once</li>
<li>Yellow card, a team can give 2 answers for a single question</li>
<li>Yellow card can be used only once in any of the rounds except for the last round</li>
</ul>
 <p>&nbsp;</p>
<p style="font-size:18px; color:#F00;">
The Quiz consists of following rounds:
</p>
<p style="font-size:18px; color:#F00;">
Round 1: General Question-Answer Round
</p>
<p style="font-size:16px; color:#F00;">
Rules
</p>
<p>&nbsp;</p>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li>This is a general questionnaire round wherein the quiz master throws a question at the teams, once in clock wise and once in anticlockwise directions. The teams will be given sufficient time to answer the questions.</li>
<li>If they cannot answer or give wrong answer then the question is passed to the next immediate team. Passing of question is limited only to next immediate neighbor.</li>
<li>Each correct answer for a direct question will fetch +10 points, correct answer for passed question carries+5 points and there’s no losing of points for wrong answers.</li>
</ul>
 <p>&nbsp;</p>
<p style="font-size:18px; color:#F00;">
Round 2: True/False
</p>
<p style="font-size:16px; color:#F00;">
Rules
</p>
<p>&nbsp;</p>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li>This is a simple true/false round wherein the quiz master gives a fact; the corresponding team needs to answer whether it is true or false</li>
<li>For a right answer +10 points, if team cannot answer or give wrong answer they will lose as many points as the question carries.</li>
<li>In this round the teams cannot pass and/or challenge the question to other teams.</li>
</ul>
 <p>&nbsp;</p>
<p style="font-size:18px; color:#F00;">
Round 3: Audio/Visual Round
</p>
<p style="font-size:16px; color:#F00;">
Rules
</p>
<p>&nbsp;</p>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li>In this round an audio, video or an image will be displayed and the team has to answer the question based on that audio/video/image</li>
<li>Each team gets two questions in this round. There will be no passing of question and challenging in this round.</li>
<li>If the team answers the question for the first attempt correctly then they fetch 20 points. </li>
<li>If the team doesn’t know the answer then they will be allowed to ask for a hint to the quiz master. If the team answers the question correctly with one hint then they get 10 points</li>
<li>If team avail one more hint and then answer then they fetch 5 points</li>
<li>If a team fails to answer the question or answer’s it wrongly then the concept of hints become null and void and they fetch 0 points.</li>
</ul>
 <p>&nbsp;</p>
<p style="font-size:18px; color:#F00;">
Round 4: Choose Domain
</p>
<p style="font-size:16px; color:#F00;">
Rules
</p>
<p>&nbsp;</p>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li>This is a participants round where each team is allowed to choose their interested domain</li>
<li>Once the domain is chosen by team, the quiz master will ask 5 questions to each team related to the chosen domain.</li>
<li>Team can answer all the 5 questions or else they can challenge the other team by making use of Red card but the challenge is limited to any 2 questions asked in this round</li>
<li>For a direct right answer +10 points and for a wrong answer -5.</li>
<li>If a team challenges the other team and other team answers the question that team will get +20 points</li>
<li>If challenge accepted team cannot answer or give wrong answer they lose 10 points and the challenged team will get +10 points. </li>
</ul>
 <p>&nbsp;</p>
<p style="font-size:16px; color:#FFF;">
<b>Note: Challenge is limited to only 2 questions.</b>
</p>
<p style="font-size:18px; color:#F00;">
Round 5: Rapid Fire
</p>
<p style="font-size:16px; color:#F00;">
Rules
</p>
<p>&nbsp;</p>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li>This round consists of questions which will be fired at the participants within a span of 2 minutes or 10 questions, whichever is the earliest. </li>
<li>Each correct answer carries 10 points and there is no negative marking </li>
</ul>
 <p>&nbsp;</p>
<p style="font-size:16px; color:#FFF;">
<b>The decisions of the judges/quizmasters will be final.</b>
</p>
