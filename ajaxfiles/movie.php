Problem Statement 
</p>
 <p>&nbsp;</p>
<p style="font-size:14px; color:#FFF;  margin-left:20px;">
Movie making competition is a short film competition to promote amateur film makers/animation movie makers to showcase their talent and creativity in the field of direction and aims to encourage creative and thought provoking films. Making a short film will show one how to explore, innovate, and experiment in film and video.
<p>&nbsp;</p>
<p style="font-size:14px; color:#FFF;">In this competition, students will be challenged to create an original short film and also demonstrate excellence in filmmaking at all levels from concept art to polished editing. The best 16 entries will be shortlisted and will be screened in the event.</p> 

</p>
 <p>&nbsp;</p>

<p style="font-size:18px; color:#F00;">
General Instructions 
</p>
 <p>&nbsp;</p>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li>Minimum no. of members allowed is 4 and maximum no. of members is 6.</li>
<li>Entries must not exceed the limit of 20 minutes.</li>
<li>The movie can be in either English or Hindi or kannada.</li>
<li>The topics for the movie making will be provided by the organisers.</li>
<li>We will not accept any defamatory, obscene, blasphemous or unlawful material.</li>
<li>All entries must reach us by 19th March, 2013. </li>
<li>Each entry should be accompanied by the following:</li>
</ul>
<ul style="font-size:14px; color:#FFF; margin-left:60px;">
<li>A synopsis of the film (max of 150 words)</li>
<li>The filmmaker's profile.</li>
</ul>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li>Shortlisted films will be screened on the 19th March, 2013 and the results will be declared on the next day.</li>
<li>The decision of the jury members will be final and binding.</li>
<li>Merit certificates will be provided to all the finalists. </li>

</ul>
