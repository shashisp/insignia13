<p style="font-size:18px; color:#F00;">
Rules and regulations:
</p>
 <p>&nbsp;</p>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li>All the participants will have to register for this event.</li>
<li>Teams should comprise of maximum two members.</li>
<li>Participants should be present at the venue before half an hour of start of the event.</li>
<li>Event consists of five rounds.</li>
<li>The decision of the judges will be final and binding. </li>
</ul>
<p>&nbsp;</p>
<p style="font-size:17px; color:#F00;">
Round 1 :
</p>
 <p>&nbsp;</p>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li>Round 1 consists of 50 multiple choice questions.</li>
<li>Questions will be based on simple quantitative mathematics.</li>
<li>Results will be announced after half an hour.</li>
<li>Selected teams will be called for the next round and will be eligible for collecting the participation certificate.</li>

</ul>
<p>&nbsp;</p>

<p style="font-size:17px; color:#F00;">
Round 2 :
</p>
 <p>&nbsp;</p>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li>Selected teams will be given a tutorial on Vedic mathematics and general techniques and they will be provided with a CD. (3 hours session)</li>
<li>Based on the tutorial questions will be asked.</li>
<li>After this round the elimination will be done and the teams who have qualified will be called for the next round 3.</li>


</ul>
<p>&nbsp;</p>

<p style="font-size:17px; color:#F00;">
Round 3 :
</p>
 <p>&nbsp;</p>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li> This round will be of subjective type questions based on engineering mathematics based on timing.</li>
<li>In this round two teams will be given same question and one member of each team will have to solve the problem on the blackboard.</li>
<li>The team who will solve the problem first will qualify for the next round.</li>
<li>Qualified teams will have to wait for the next round.</li>



</ul>
<p>&nbsp;</p>

<p style="font-size:17px; color:#F00;">
Round 4 :
</p>
 <p>&nbsp;</p>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li>In this round each team will be given two subjective questions based on engineering math.</li>
<li>This round will be based on timing and correctness.</li> 
<li>The timing of each team will be noted and top four teams who will solve the problem in least time will be qualified for the final round.</li>
</ul>
<p>&nbsp;</p>


<p style="font-size:17px; color:#F00;">
Round 5 (Finale) :
</p>
 <p>&nbsp;</p>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li>This round will be a mock round.</li>
<li>Each team will be asked 10 questions.</li>
<li>Based on the highest number of questions given correctly by the teams 1st and 2nd prize will be given.</li>
<li>In the case of a tie, another series of 10 questions will be asked.</li>
<li>The winner will be given prize money, memento and a winning certificate.</li>

</ul>
