Rules and regulations:
</p>
 <p>&nbsp;</p>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li>Students from E&C, CSE, E&E, Instrumentation and ISE can participate.</li>
<li>There are 4 domains on which participants can exhibit their project.</li>
</ul>
 <ul style="font-size:14px; color:#FFF;  margin-left:60px;">
<li>Computer Science and Information Science & Engineering.</li>
<li>Electronics & Communication and Electrical Engineering.</li>
</ul>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">
<li>For C.Sc. and Info. Sc.&Engg. domain, project/software developed should contain 80% of software and may contain 20% hardware.</li>
<li>For E&C and E&E Engg. domain, project should contain at least 75% hardware and may contain 25% software.</li>
<li>Teams have to get the software and necessary accessories (not provided by college) to give a demonstration of the software/project.</li>
 <li>Teams have to prepare a PowerPoint presentation (not exceeding 15 slides).</li> 
<li>20 minutes time will be given for presentation and demonstration of the project. </li>             
<li>Participants will be responsible for any damage made to their project (e.g. damage to circuit board by excess voltage).</li>
<li>Existing projects/ readymade kits/tools will not be entertained to exhibit.</li>



</ul>
