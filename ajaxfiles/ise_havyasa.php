Procedure:
</p>
 <p>&nbsp;</p>
<ul style="font-size:14px; color:#FFF;  margin-left:20px;">

<li>A team with maximum 3 members can register for this event.</li>
<li>Brief description about the project should be submitted via e-mail to <font color="#0000FF">insigniaise@gmail.com</font></li>
<li>Teams qualifying for the exhibition of the project will be informed via E-mail.</li> 
<li>On the day of event, team members have to exhibit their project in the space provided to them.</li>
